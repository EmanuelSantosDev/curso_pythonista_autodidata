# Ordenando Listas


# Invertendo a Lista
valores = [31, 23, 6, 36, 21, 33, 37, 7, 20, 23]
valores.reverse()
print(valores)  # [23, 20, 7, 37, 33, 21, 36, 6, 23, 31]


# Ordem Crescente
valores.sort()
print(valores)  # [6, 7, 20, 21, 23, 23, 31, 33, 36, 37]


# Ordem Decrescente
valores.sort(reverse=True)
print(valores)  # [37, 36, 33, 31, 23, 23, 21, 20, 7, 6]
