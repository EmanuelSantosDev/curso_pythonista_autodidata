# Tipos de Números


import math


a = 20
b = 20.5
print(type(a))  # <class 'int'>
print(type(b))  # <class 'float'>


# Operações Matemáticas


print(10 + 6)  # 16
print(10 - 6)  # 4
print(10 * 6)  # 60
print(10 / 6)  # 1.6666666666666667
print(10 // 6)  # 1 (divisão de inteiro)
print(10 % 6)  # 4 (modulos / resto da divisão)
print(10 ** 6)  # 1000000 (exponenciação)


# Atribuições Mais Rápidas


a = 10
a = a + 5
a += 5

b = 20
b = b - 2
b -= 2


# Operações Matemáticas Comuns


print(round(5.7))  # 6

# importamos o módulo 'math' no início do código
print(math.ceil(2.2))  # 3
