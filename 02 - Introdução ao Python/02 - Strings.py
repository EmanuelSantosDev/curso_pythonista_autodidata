# <<< String de uma linha >>>


frase_1 = 'Codar todos os dias'

print(frase_1)
# Codar todos os dias


# <<< String de Múltiplas Linhas >>>


frase_2 = '''Primeira linha
Segunda linha
Terceira linha'''

print(frase_2)
# Primeira linha
# Segunda linha
# Terceira linha


# <<< Caracteres de Escape >>>


frase_3 = 'Olá, meu nome é \nEmanuel Olivio dos Santos'

print(frase_3)
# Olá, meu nome é
# Emanuel Olivio dos Santos


frase_4 = 'Eu estou \'MORRENDO\' de fome!'

print(frase_4)
# Eu estou 'MORRENDO' de fome!


frase_5 = 'Arquivo localizado em c:\\drive\\arquivo.txt'

print(frase_5)
# Arquivo localizado em c:\drive\arquivo.txt


# <<< Comprimento de uma String >>>


nome = 'Emanuel'

print(len(nome))
# 7
